#include "../libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*current_node;

	current_node = malloc(sizeof(t_list));
	if (current_node == NULL)
		return (NULL);
	current_node->content = content;
	current_node->next = NULL;
	return (current_node);
}

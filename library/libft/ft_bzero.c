#include "../libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*newstr;

	newstr = (char *)s;
	i = 0;
	while (i < n)
	{
		newstr[i] = '\0';
		i++;
	}
}
